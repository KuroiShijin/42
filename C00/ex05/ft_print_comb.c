#include <unistd.h>

void	while_comb(char a, char b, char c)
{
	while (a <= '7')
	{
		while (b <= '8')
		{
			while (c <= '9')
			{
				write(1, &a, 1);
				write(1, &b, 1);
				write(1, &c, 1);
				if (a != '7')
					write(1, ", ", 2);
				c++;
			}
			b++;
			c = b +1;
		}
		b = a +1;
		a++;
	}
}

void	ft_print_comb(void)
{
	while_comb('0', '1', '2');
}
